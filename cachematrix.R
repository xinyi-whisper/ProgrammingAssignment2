##Caching the Inverse of a Matrix:
##Matrix inversion is usually a costly computation and there may be
##some benefits to caching the inverse of a matrix rather than compute it repeatedly.
##Below are a pair of functions that are used to create a special object that
##stores a matrix and caches its inverse.

##This function creates a special matrix object that can mache its inverse.

makeCacheMatrix <- function(x = matrix()) {
  inv <- NULL
  set <- function(y){
    x <<- y
    inv <- NULL
  }
  get <- function()x
  setInverse <- function(inverse) inv <<- inverse
  getInverse <- function() inv
  list(set=set, get=get,setInverse=setInverse,getInverse=getInverse)
}



## Write a short comment describing this function
##This function computes the Inverse of the special matrix created by
##makeCacheMatrix function above. If the inverse has already been calculated,
##then it should retrieve the inverse from the cache.

cacheSolve <- function(x, ...) {
  ##Return a matrix that is the inverse of 'x'
  inv <- x$getInverse()
  if (!is.null(inv)) {
    message("getting cached data")
    return(inv)
  }
  mat <- x$get()
  inv <- solve(mat,...)
  x$setInverse(inv)
  inv
}
